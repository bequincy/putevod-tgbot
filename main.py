@bot.message_handler(func=lambda msg: msg.chat.id in user_states and 'step' in user_states[msg.chat.id])
def register_user(message):
    user_id = message.chat.id
    state = user_states[user_id]
    text = message.text.strip()

    # ‚ùå If user tries to reset with /start or "üìç –í –Ω–∞—á–∞–ª–æ", just repeat the same question
    if text in ["/start", "üìç –í –Ω–∞—á–∞–ª–æ"]:
        if state['step'] == 'name':
            bot.send_message(user_id, "–ü–æ–ø—Ä–æ–±—É–π –µ—â—ë —Ä–∞–∑. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏ —Å–≤–æ—ë –§–ò–û:")
        elif state['step'] == 'phone':
            bot.send_message(user_id, "–ü–æ–ø—Ä–æ–±—É–π –µ—â—ë —Ä–∞–∑. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏ —Å–≤–æ–π –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞:")
        elif state['step'] == 'city':
            bot.send_message(user_id, "–ü–æ–ø—Ä–æ–±—É–π –µ—â—ë —Ä–∞–∑. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏ —Å–≤–æ–π –≥–æ—Ä–æ–¥:")
        return  # üîÑ Do not change step

    # ‚úÖ Normal registration flow
    if state['step'] == 'name':
        state['name'] = text
        state['step'] = 'phone'
        bot.send_message(user_id, "–¢–µ–ø–µ—Ä—å –≤–≤–µ–¥–∏ –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞:")

    elif state['step'] == 'phone':
        state['phone'] = text
        state['step'] = 'city'
        bot.send_message(user_id, "–í–≤–µ–¥–∏ –≥–æ—Ä–æ–¥:")

    elif state['step'] == 'city':
        state['city'] = text

        with sqlite3.connect('users.db') as conn:
            cursor = conn.cursor()
            cursor.execute('''INSERT OR REPLACE INTO users (telegram_id, name, phone, city)
                              VALUES (?, ?, ?, ?)''',
                           (user_id, state['name'], state['phone'], state['city']))
            conn.commit()

        del user_states[user_id]
        bot.send_message(user_id, "–°–ø–∞—Å–∏–±–æ! –î–∞–Ω–Ω—ã–µ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã.")
